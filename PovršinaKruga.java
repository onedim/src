// Izračunavanje površine kruga
public class PovršinaKruga {
	public static void main (String [] args) {
		int radius = 5;
		System.out.println ("Površina kruga je");
		System.out.println ((radius * radius) * Math.PI);
		System.out.println ("Obim kruga je");
		System.out.println ((radius * 2) * Math.PI);
	}

}
